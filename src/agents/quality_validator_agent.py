    async def _calculate_quality_metrics(self, analysis_data: Dict[str, Any], validation_result: ValidationResult) -> QualityMetrics:
        """Calculate comprehensive quality metrics."""
        try:
            # Extract scores from rule results
            rule_scores = {
                'employee_data_completeness': 0.0,
                'data_consistency': 0.0,
                'performance_accuracy': 0.0,
                'insight_quality': 0.0,
                'data_integrity': 0.0,
                'recommendation_relevance': 0.0
            }
            
            for rule_name, rule_result in validation_result.rule_results.items():
                if rule_name in rule_scores:
                    rule_scores[rule_name] = rule_result.get('score', 0.0)
            
            # Calculate quality metrics
            completeness_score = rule_scores['employee_data_completeness']
            accuracy_score = rule_scores['performance_accuracy']
            consistency_score = rule_scores['data_consistency']
            insight_quality_score = rule_scores['insight_quality']
            data_integrity_score = rule_scores['data_integrity']
            
            # Calculate overall quality score
            overall_quality_score = (
                completeness_score * 0.2 +
                accuracy_score * 0.2 +
                consistency_score * 0.15 +
                insight_quality_score * 0.2 +
                data_integrity_score * 0.15 +
                rule_scores['recommendation_relevance'] * 0.1
            )
            
            return QualityMetrics(
                completeness_score=completeness_score,
                accuracy_score=accuracy_score,
                consistency_score=consistency_score,
                insight_quality_score=insight_quality_score,
                data_integrity_score=data_integrity_score,
                overall_quality_score=overall_quality_score
            )
            
        except Exception as e:
            logger.warning(f"Failed to calculate quality metrics: {e}")
            return QualityMetrics(
                completeness_score=0.5,
                accuracy_score=0.5,
                consistency_score=0.5,
                insight_quality_score=0.5,
                data_integrity_score=0.5,
                overall_quality_score=0.5
            )
    
    async def _determine_final_status(self, validation_result: ValidationResult, quality_metrics: QualityMetrics) -> Tuple[ValidationStatus, List[str]]:
        """Determine final validation status and recommended actions."""
        actions = []
        
        # Define status thresholds
        if quality_metrics.overall_quality_score >= 0.9 and len(validation_result.issues_found) == 0:
            status = ValidationStatus.APPROVED
            actions.append("Analysis approved for publication")
        
        elif quality_metrics.overall_quality_score >= 0.8 and len(validation_result.issues_found) <= 2:
            status = ValidationStatus.APPROVED
            actions.append("Analysis approved with minor recommendations")
            actions.extend(validation_result.recommendations[:3])
        
        elif quality_metrics.overall_quality_score >= 0.7:
            status = ValidationStatus.REVISION_REQUIRED
            actions.append("Revision required before approval")
            actions.extend(validation_result.recommendations[:5])
            
            if self.auto_revision_enabled:
                actions.append("Consider enabling auto-revision workflow")
        
        elif quality_metrics.overall_quality_score >= 0.5:
            status = ValidationStatus.MANUAL_REVIEW
            actions.append("Manual review required due to quality concerns")
            actions.extend(validation_result.issues_found[:3])
        
        else:
            status = ValidationStatus.REJECTED
            actions.append("Analysis rejected due to poor quality")
            actions.append("Requires comprehensive review and regeneration")
            actions.extend(validation_result.issues_found[:5])
        
        return status, actions
    
